export function getProgramPath(course: StepikCourse): string {
  return `/training/${course.id}`;
}

export function getProgramPage(course: StepikCourse): ProgramPageData {
  const context = CATEGORY_CONTEXT[course.category];
  const results = buildResults(course);
  const nextCourses = context.nextIds
    .filter((id) => id !== course.id)
    .map((id) => STEPIK_COURSES.find((item) => item.id === id))
    .filter((item): item is StepikCourse => Boolean(item))
    .slice(0, 3);

  return {
    course,
    path: getProgramPath(course),
    problem: context.problem,
    audience: context.audience,
    results,
    skills: getSkillsBySlugs(context.skillSlugs).map((skill) => ({
      title: skill.title,
      href: `/skills/${skill.slug}`
    })),
    modules: context.modules,
    extras: context.extras,
    reviews: buildReviews(course),
    nextCourses
  };
}

export function getProgramPageById(id: string): ProgramPageData | undefined {
  const numericId = Number(id);
  if (!Number.isFinite(numericId)) {
    return undefined;
  }

  const course = STEPIK_COURSES.find((item) => item.id === numericId);
  return course ? getProgramPage(course) : undefined;
}

function buildResults(course: StepikCourse): string[] {
  const base = [
    course.result,
    ...course.gives,
    `применять инструменты программы в рабочем контексте: ${course.forWhom.toLowerCase()}`,
    "понимать следующий шаг развития после завершения обучения",
    "собрать практический артефакт, который можно использовать в работе"
  ];

  return Array.from(new Set(base.filter(Boolean))).slice(0, Math.max(6, Math.min(base.length, 8)));
}

function buildReviews(course: StepikCourse): ProgramReview[] {
  if (course.reviews === "0" || course.reviews.trim() === "") {
    return [];
  }

  return [
    {
      name: "Слушатель Stepik",
      role: course.forWhom.split(",")[0] || "специалист",
      problem: "нужно было быстро разобраться в теме и применить материал в работе",
      result: course.result
    }
  ];
}
