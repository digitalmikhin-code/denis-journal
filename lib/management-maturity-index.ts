export const MATURITY_QUESTIONS: MaturityQuestion[] = MATURITY_BLOCKS.flatMap((block, blockIndex) =>
  QUESTION_BANK[block.key].map((text, questionIndex) => {
    const isReverse = questionIndex % 4 === 3;
    const format: MaturityQuestionFormat =
      questionIndex % 10 === 1 ? "case" : questionIndex % 10 === 6 ? "dilemma" : isReverse ? "reverse" : "scale";

    return {
      id: blockIndex * 20 + questionIndex + 1,
      block: block.key,
      format,
      text,
      reverse: isReverse
    };
  })
);

export function resolveMaturityLevel(totalIndex: number): MaturityLevel {
  if (totalIndex <= 250) return MANAGEMENT_MATURITY_INDEX.levels[0];
  if (totalIndex <= 450) return MANAGEMENT_MATURITY_INDEX.levels[1];
  if (totalIndex <= 650) return MANAGEMENT_MATURITY_INDEX.levels[2];
  if (totalIndex <= 800) return MANAGEMENT_MATURITY_INDEX.levels[3];
  return MANAGEMENT_MATURITY_INDEX.levels[4];
}

export function resolveMaturityProfile(topBlocks: MaturityBlockKey[]): MaturityProfile {
  const topSet = new Set(topBlocks.slice(0, 4));
  const exact = MANAGEMENT_MATURITY_INDEX.profiles.find((profile) =>
    profile.signal.every((block) => topSet.has(block))
  );

  return exact ?? MANAGEMENT_MATURITY_INDEX.profiles[0];
}
